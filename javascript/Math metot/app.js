//! floor: sayının noktadan sonrasını uçurur
let a = 3.15
console.log(Math.floor(a))

//! ceil: sayının noktadan sonrasını yuvarlar üste

console.log(Math.ceil(a))

//! round: sayının noktadan sonrasını yuvarlar en yakına
console.log(Math.round(a))

//! max: en büyük sayıyı getirir
console.log(Math.max(1, 2, 3, 4, 5, 6, 7, 8, 9))

//! min: en küçük sayıyı getirir
console.log(Math.min(1, 2, 3, 4, 5, 6, 7, 8, 9))

//! abs: sayının mutlak değerini getirir
console.log(Math.abs(-10))

//! pow: sayının üssünü alır
console.log(Math.pow(2, 3))
//!sqrt: sayının karekökünü alır
console.log(Math.sqrt(16))
console.log(Math.sqrt(81))

console.log(Math.PI)

//! random: 0 ile 1 arasında rastgele sayı üretir
console.log(Math.floor(Math.random() * 100))

